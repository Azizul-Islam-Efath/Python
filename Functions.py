# User defined function

''' syntax --   def function_name():
                    operation
                    print / return
'''
def add(a, b):
    plus = a + b
    print(plus)


def subtract(a, b):
    sub = a - b
    print(sub)

def multiply(a, b):
    multi = a * b
    print(multi)

def divide(a, b):
    dive = a / b
    print(dive)



add(10, 20)

subtract(10, 20)

multiply(5, 125)

divide(10, 2)